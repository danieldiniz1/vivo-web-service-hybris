package br.com.vivo.b2b.controllers.datadto;

public class CustomerDataDTO {
    private String cpf;
    private String rg;

    private String name;

    public CustomerDataDTO() {
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getRg() {
        return rg;
    }

    public void setRg(String rg) {
        this.rg = rg;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
